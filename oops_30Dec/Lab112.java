package oops_30Dec;

public class Lab112 {
    public static void main(String[] args) {

        int c = sum(4,8);
        System.out.println(c);

        int d = sum(12);
        System.out.println(d);
    }

    static int sum(int a, int b){
        return a+b;
    }

    static int sum(int d){
        return d;
    }

}
