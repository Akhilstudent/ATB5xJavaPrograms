package src.basics_02;

public class Lab036 {
    public static void main(String[] args) {

        // Ternary Operator

        int a = true ? 10:20;
        System.out.println(a);

        int b = (30>40) ? 20:40;
        System.out.println(b);

        String s1 = (50<58) ? "Value" : "10";
        System.out.println(s1);

    }
}
